
public class IncrementDecrement {
    public static void main(String[] args)  
{              
    int n = 4, k = 2;            
          
    System.out.println(++n );           
    System.out.println( n );            
            
    System.out.println( n++ );        
    System.out.println( n );            
    System.out.println(-n );           
    System.out.println( n );      
                 
    System.out.println( --n );        
    System.out.println( n );            
                 
    System.out.println( n--);   
    System.out.println( n );            
                 
    System.out.println( n + k );     
    System.out.println( n );            
    System.out.println( k );      
    System.out.println("" + n + " " + k ); 
    System.out.println( n );       
    System.out.println( "  " + n );     
    System.out.println( " n" );    
    System.out.println( "\n" );    
            
    System.out.println( " n * n = "); //CAREFUL!      
    System.out.println( n * n );    
    System.out.println( 'n' );    
} 
}
